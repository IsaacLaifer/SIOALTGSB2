    private void btnConnexionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConnexionMouseClicked
        // TODO add your handling code here:
        if(txtLogin.getText().compareTo("")==0)
        {
            lblErreur.setText("saisir votre login");
        }
        else if (txtPwd.getText().compareTo("")==0)
        {
            lblErreur.setText("saisir votre passeword");
        }
        else 
        {
            cnx=new ConnexionBdd();
            fm=new FonctionsMetier();
            
            Utilisateur unUtilisateur = fm.VerifierIdentifiants(txtLogin.getText(), txtPwd.getText());
            
            if(unUtilisateur !=null)
            {
                if (unUtilisateur.getStatutUser().compareTo("admin")==0)
                {
                    //si il est admin
                    frmAdministrateur frm= new frmAdministrateur();
                    frm.setVisible(true);
                }
                else
                {
                    //si il est user
                    frmUser frm =new frmUser(unUtilisateur);
                    frm.setVisible(true);
                }
            }
            else
            {
                lblErreur.setText("identifiants incorrect");
            }
        }
    }//GEN-LAST:event_btnConnexionMouseClicked
    
